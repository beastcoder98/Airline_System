    public void FlightAutoID(){
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/javaproject","root","");
            Statement s = con.createStatement();
            ResultSet rs = s.executeQuery("select MAX(flightID) from  flights");
            rs.next();
            rs.getString("MAX(flightID)");
            
            if(rs.getString("MAX(flightID)") == null ){
                 flightID.setText("FL03001");
            }
            else {
                long id = Long.parseLong(rs.getString("MAX(flightID)").substring(2,rs.getString("MAX(flightID)").length()));
                id++;
                flightID.setText("FL" + String.format("%04d", id));
            }
            
            
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Add_Flights.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Add_Flights.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }

