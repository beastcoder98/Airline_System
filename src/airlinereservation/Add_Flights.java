    private void AddFlightDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddFlightDetailsActionPerformed
        // TODO add your handling code here:
        String flID = flightID.getText();
        String flName = flightName.getText();
        String flSource = flightSource.getSelectedItem().toString();
        String fldepTime = flightDepTime.getText();
        
        DateFormat depDate = new SimpleDateFormat("dd-MM-yyyy");
        String fldepDate = depDate.format(flightDepDate.getDate());
        
        DateFormat arrDate = new SimpleDateFormat("dd-MM-yyyy");
        String flarrDate = arrDate.format(flightArriveDate.getDate());
        
        String flarrTime = flightArriveTime.getText();
        String flcharges = flightCharges.getText();
        String flDestination = flightDestination.getSelectedItem().toString();
        
        
        try {
            Class.forName("com.mysql.jdbc.Driver");
            String query = "insert into flights values(?,?,?,?,?,?,?,?,?)";
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/javaproject","root","");
            pst = con.prepareStatement(query);
            
            pst.setString(1, flID);
            pst.setString(2, flName);
            pst.setString(3,flSource);
            pst.setString(4,fldepTime);
            pst.setString(5,fldepDate);
            pst.setString(6,flarrTime);
            pst.setString(7,flDestination);
            pst.setString(8,flarrDate);
            pst.setString(9,flcharges);
           
          
           pst.executeUpdate();
           
           JOptionPane.showMessageDialog(null,"......."+"the " + flName + " flight has been added");
           
             flightID.setText("");
            flightName.setText("");
            flightSource.setSelectedIndex(0);
            flightDepTime.setText("");
            flightDepDate.setDate(null);
            flightArriveTime.setText("");
            flightDestination.setSelectedIndex(0);
            flightArriveDate.setDate(null);
            flightCharges.setText("");
            FlightAutoID();
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Add_Flights.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Add_Flights.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_AddFlightDetailsActionPerformed

